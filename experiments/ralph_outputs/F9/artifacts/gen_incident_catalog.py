#!/usr/bin/env python3
"""F9 supplementary-material generator: enumerate every CIDG incident spec.

Reads every YAML under scenarios/cidg/generated/ and emits a markdown
supplementary catalog with the key fields for each incident:
  id, title, root cause (location + kind + severity + hidden?),
  fix (ordered canonical_fix steps), and source.

It also emits a machine-readable JSON index alongside the markdown so the
catalog can be regenerated / diffed deterministically.

Usage:
    python3 gen_incident_catalog.py \
        [--src DIR] [--out-md FILE] [--out-json FILE]

Defaults resolve relative to the repo root inferred from this file's path,
so it is runnable from anywhere. Does NOT edit any shared core files.
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

try:
    import yaml
except ImportError:  # pragma: no cover
    sys.stderr.write("PyYAML is required: pip install pyyaml\n")
    raise

# Repo root: experiments/ralph_outputs/F9/artifacts/ -> up 4
REPO_ROOT = Path(__file__).resolve().parents[4]
DEFAULT_SRC = REPO_ROOT / "scenarios" / "cidg" / "generated"
DEFAULT_OUT_MD = Path(__file__).resolve().parent / "incident_catalog.md"
DEFAULT_OUT_JSON = Path(__file__).resolve().parent / "incident_catalog.json"


def _fmt_fix_steps(canonical_fix: dict) -> str:
    """Render canonical_fix.steps as a compact ordered string."""
    if not isinstance(canonical_fix, dict):
        return "(none)"
    steps = canonical_fix.get("steps") or []
    parts = []
    for i, step in enumerate(steps, 1):
        if not isinstance(step, dict):
            continue
        tool = step.get("tool", "?")
        args = step.get("args") or {}
        target = args.get("target", "")
        extra = {k: v for k, v in args.items() if k != "target"}
        seg = f"{i}. `{tool}`"
        if target:
            seg += f" → {target}"
        if extra:
            seg += f" ({', '.join(f'{k}={v}' for k, v in extra.items())})"
        parts.append(seg)
    return "<br>".join(parts) if parts else "(none)"


def extract(spec: dict, path: Path) -> dict:
    """Pull the key fields out of one parsed spec, tolerating missing keys."""
    meta = spec.get("meta") or {}
    rc = spec.get("root_cause") or {}
    cf = spec.get("canonical_fix") or {}
    assertions = spec.get("assertions") or {}

    rc_kind = rc.get("kind", "?")
    rc_loc = rc.get("location", "?")
    rc_sev = rc.get("severity", "")
    rc_hidden = bool(rc.get("hidden", False))
    root_cause_str = f"{rc_kind} @ {rc_loc}"
    if rc_sev != "":
        root_cause_str += f" (sev {rc_sev})"
    if rc_hidden:
        root_cause_str += " [hidden]"

    return {
        "file": path.name,
        "id": meta.get("id", path.stem),
        "title": meta.get("title", ""),
        "failure_class": meta.get("failure_class", ""),
        "source": meta.get("source", ""),
        "urls": meta.get("urls") or [],
        "root_cause": root_cause_str,
        "root_cause_kind": rc_kind,
        "root_cause_location": rc_loc,
        "root_cause_hidden": rc_hidden,
        "fix": _fmt_fix_steps(cf),
        "fix_ordering_notes": cf.get("ordering_notes", ""),
        "cascades": bool(assertions.get("cascades", False)),
        "n_nodes": len((spec.get("topology") or {}).get("nodes") or []),
    }


def collect(src: Path) -> list[dict]:
    rows = []
    errors = []
    for path in sorted(src.glob("*.yaml")):
        try:
            with open(path) as f:
                spec = yaml.safe_load(f)
            if not isinstance(spec, dict):
                errors.append((path.name, "not a mapping"))
                continue
            rows.append(extract(spec, path))
        except Exception as e:  # noqa: BLE001
            errors.append((path.name, str(e)))
    if errors:
        sys.stderr.write("WARN: skipped %d files:\n" % len(errors))
        for name, msg in errors:
            sys.stderr.write(f"  - {name}: {msg}\n")
    return rows


def render_md(rows: list[dict], src: Path) -> str:
    lines = []
    lines.append("# Supplementary Material — CIDG Incident Catalog")
    lines.append("")
    lines.append(
        "Auto-generated by `gen_incident_catalog.py` from "
        f"`{src.relative_to(REPO_ROOT)}/*.yaml`. "
        f"Total incidents: **{len(rows)}**."
    )
    lines.append("")
    lines.append(
        "Each row lists the incident id, human title, the (often hidden) "
        "root cause, the ordered canonical fix, and the real-world source "
        "the synthetic spec is grounded in."
    )
    lines.append("")

    # Summary by failure class
    by_class: dict[str, int] = {}
    n_cascade = 0
    n_hidden = 0
    for r in rows:
        by_class[r["failure_class"] or "(unset)"] = by_class.get(r["failure_class"] or "(unset)", 0) + 1
        n_cascade += int(r["cascades"])
        n_hidden += int(r["root_cause_hidden"])
    lines.append("## Summary")
    lines.append("")
    lines.append(f"- Incidents: **{len(rows)}**")
    lines.append(f"- Cascading incidents: **{n_cascade}**")
    lines.append(f"- Hidden root cause: **{n_hidden}**")
    lines.append(f"- Distinct failure classes: **{len(by_class)}**")
    lines.append("")
    lines.append("| Failure class | Count |")
    lines.append("|---|---|")
    for cls, n in sorted(by_class.items(), key=lambda kv: (-kv[1], kv[0])):
        lines.append(f"| `{cls}` | {n} |")
    lines.append("")

    # Full catalog table
    lines.append("## Full catalog")
    lines.append("")
    lines.append("| # | ID | Title | Root cause | Canonical fix | Source |")
    lines.append("|---|---|---|---|---|---|")
    for i, r in enumerate(rows, 1):
        title = (r["title"] or "").replace("|", "\\|")
        source = (r["source"] or "").replace("|", "\\|")
        if r["urls"]:
            source += " " + " ".join(f"[link]({u})" for u in r["urls"])
        rc = r["root_cause"].replace("|", "\\|")
        fix = r["fix"].replace("|", "\\|")
        lines.append(
            f"| {i} | `{r['id']}` | {title} | {rc} | {fix} | {source} |"
        )
    lines.append("")

    # Per-incident detail blocks
    lines.append("## Per-incident detail")
    lines.append("")
    for r in rows:
        lines.append(f"### {r['id']}")
        lines.append("")
        lines.append(f"- **File:** `{r['file']}`")
        lines.append(f"- **Title:** {r['title']}")
        lines.append(f"- **Failure class:** `{r['failure_class']}`")
        lines.append(f"- **Root cause:** {r['root_cause']}")
        lines.append(f"- **Topology nodes:** {r['n_nodes']}  |  **Cascades:** {r['cascades']}")
        if r["fix_ordering_notes"]:
            lines.append(f"- **Fix ordering:** {r['fix_ordering_notes']}")
        lines.append(f"- **Canonical fix:** {r['fix']}")
        lines.append(f"- **Source:** {r['source']}")
        if r["urls"]:
            lines.append("- **URLs:** " + ", ".join(r["urls"]))
        lines.append("")
    return "\n".join(lines) + "\n"


def main(argv=None) -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--src", type=Path, default=DEFAULT_SRC)
    ap.add_argument("--out-md", type=Path, default=DEFAULT_OUT_MD)
    ap.add_argument("--out-json", type=Path, default=DEFAULT_OUT_JSON)
    args = ap.parse_args(argv)

    if not args.src.is_dir():
        sys.stderr.write(f"ERROR: src dir not found: {args.src}\n")
        return 2

    rows = collect(args.src)
    if not rows:
        sys.stderr.write("ERROR: no incident specs parsed\n")
        return 1

    md = render_md(rows, args.src)
    args.out_md.write_text(md)
    args.out_json.write_text(json.dumps(rows, indent=2) + "\n")

    print(f"Parsed {len(rows)} incident specs from {args.src}")
    print(f"Wrote markdown catalog: {args.out_md}")
    print(f"Wrote JSON index:       {args.out_json}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
